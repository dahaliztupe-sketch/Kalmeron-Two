#!/usr/bin/env node
/**
 * Generate type-safe TypeScript clients from the OpenAPI specs in
 * `docs/api/services/*.openapi.json`.
 *
 * Output: `src/lib/api-clients/<service>.types.ts`
 *
 * The generated `.types.ts` files are committed to the repo so PRs can review
 * contract changes. Hand-written wrappers in `src/lib/api-clients/<service>.ts`
 * import from these and add fetch/error/retry behaviour.
 *
 * Run after `npm run codegen:openapi`.
 */
import { readdir, mkdir, writeFile } from "node:fs/promises";
import { join, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";
import openapiTS, { astToString } from "openapi-typescript";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");
const SPEC_DIR = join(REPO_ROOT, "docs", "api", "services");
const OUT_DIR = join(REPO_ROOT, "src", "lib", "api-clients");

await mkdir(OUT_DIR, { recursive: true });

const files = (await readdir(SPEC_DIR)).filter((f) =>
  f.endsWith(".openapi.json"),
);

if (files.length === 0) {
  console.error(
    `No specs found in ${SPEC_DIR}. Run \`npm run codegen:openapi\` first.`,
  );
  process.exit(1);
}

for (const file of files) {
  const service = basename(file, ".openapi.json");
  const specPath = join(SPEC_DIR, file);
  process.stdout.write(`→ ${service.padEnd(20)} `);
  const ast = await openapiTS(new URL(`file://${specPath}`));
  const ts = astToString(ast);
  const banner = `/**
 * AUTO-GENERATED by scripts/generate-api-clients.mjs — do not edit by hand.
 * Source: docs/api/services/${file}
 *
 * Service: ${service}
 */
/* eslint-disable */

`;
  const out = join(OUT_DIR, `${service}.types.ts`);
  await writeFile(out, banner + ts, "utf8");
  console.log(`wrote ${basename(out)}`);
}

console.log(`\nGenerated ${files.length} typed client(s) in ${OUT_DIR}`);
