// @ts-nocheck
export type FounderShare = {
  name: string;
  role: string;
  equityPct: number;
  vestingMonths: number;
  cliffMonths: number;
};

export type AgreementInput = {
  companyName: string;
  founders: FounderShare[];
  ipAssignment: boolean;
  nonCompete: boolean;
  disputeMethod: "arbitration" | "courts";
  jurisdiction: string;
  founderSalaries: boolean;
  exitMechanism: "majority" | "unanimous" | "drag-along";
};

export function generateFounderAgreement(input: AgreementInput): string {
  const today = new Date().toLocaleDateString("ar-EG", { year: "numeric", month: "long", day: "numeric" });
  const totalEquity = input.founders.reduce((s, f) => s + f.equityPct, 0);
  const exitText = {
    majority: "بأغلبيّة بسيطة (٥١٪)",
    unanimous: "بإجماع جميع المؤسّسين",
    "drag-along": "بأغلبيّة الثلثين مع حقّ Drag-Along",
  }[input.exitMechanism];

  return `# اتّفاقيّة المؤسّسين الشاملة
## ${input.companyName}

**التاريخ:** ${today}  
**عدد المؤسّسين:** ${input.founders.length}  
**إجمالي الحصص الموزّعة:** ${totalEquity}٪${totalEquity !== 100 ? " ⚠️ (يجب أن يساوي ١٠٠٪)" : " ✅"}

---

## ١. الأطراف

تحرّرت هذه الاتّفاقيّة بين المؤسّسين الموقّعين أدناه، الذين سيُشار إليهم مجتمعين باسم "المؤسّسين"، ومنفردين باسم "المؤسّس":

${input.founders.map((f, i) => `**${i + 1}. ${f.name}** — ${f.role} — حصّة: ${f.equityPct}٪`).join("\n")}

---

## ٢. توزيع الحصص (Equity Split)

| المؤسّس | الدور | الحصّة | الاكتساب التدريجي (Vesting) | فترة الانتظار (Cliff) |
|---|---|---|---|---|
${input.founders.map((f) => `| ${f.name} | ${f.role} | ${f.equityPct}٪ | ${f.vestingMonths} شهراً | ${f.cliffMonths} شهراً |`).join("\n")}

> **شرح Vesting:** الحصص لا تُمنح كاملةً يوم التوقيع. كل مؤسّس "يكسب" حصّته تدريجيّاً عبر فترة الـ Vesting المحدّدة أعلاه.  
> **شرح Cliff:** خلال فترة الانتظار، إذا غادر المؤسّس الشركة، لا يحصل على أيّ حصص. بعد انتهائها، يُكتسَب جزء كبير دفعة واحدة، ثم باقي الحصص شهريّاً.

---

## ٣. الأدوار والمسؤوليّات

كل مؤسّس ملتزم بدوره المحدّد أعلاه بدوام كامل (Full-Time)، ولا يجوز له العمل في مشروع منافس دون موافقة كتابيّة من باقي المؤسّسين.

---

## ٤. الملكيّة الفكريّة (IP Assignment)

${input.ipAssignment
  ? `✅ يقرّ كل مؤسّس بأنّ جميع الأعمال والأفكار والاختراعات والكود البرمجي والمحتوى الذي ينتجه أثناء عمله في الشركة تُعتبر **ملكيّة كاملة للشركة**، حتى لو طُوِّرت خارج ساعات العمل الرسميّة.

كل مؤسّس مُلزَم بتوقيع وثيقة "تنازل عن حقوق الملكيّة الفكريّة" منفصلة لصالح الشركة.`
  : "⚠️ لم يتمّ تفعيل بند الملكيّة الفكريّة — هذا خطر كبير. يُنصَح بشدّة بإعادة النظر."}

---

## ٥. عدم المنافسة (Non-Compete)

${input.nonCompete
  ? `✅ يلتزم كل مؤسّس خلال فترة عمله في الشركة، ولمدّة **سنة واحدة بعد المغادرة**، بعدم:
- العمل في شركة منافسة مباشرةً.
- بدء مشروع منافس.
- محاولة استقطاب عملاء أو موظّفين من الشركة.`
  : "⚠️ لم يتمّ تفعيل بند عدم المنافسة — قد يؤدّي إلى مشاكل مستقبليّة."}

---

## ٦. رواتب المؤسّسين

${input.founderSalaries
  ? `يحقّ لكل مؤسّس راتب شهري عادل يتمّ تحديده بقرار الجمعيّة العامّة، على أن يُراجَع سنويّاً.`
  : `يعمل المؤسّسون **بدون راتب** (Sweat Equity) حتى تحقّق الشركة إيراداً شهريّاً مستقرّاً يتجاوز ٣x مجموع الرواتب المقترحة، أو حتى الجولة التمويليّة الأولى.`}

---

## ٧. آليّة فضّ النزاعات

${input.disputeMethod === "arbitration"
  ? `**التحكيم:** أيّ نزاع يُحلّ عبر التحكيم في **${input.jurisdiction}** وفق قواعد مركز القاهرة الإقليمي للتحكيم التجاري الدولي (CRCICA).`
  : `**القضاء:** يُختصّ بالنظر في أيّ نزاع المحاكم المختصّة في **${input.jurisdiction}**.`}

---

## ٨. مغادرة المؤسّس (Founder Departure)

في حال غادر مؤسّس قبل اكتمال vesting:
- **الحصص المكتَسَبة (Vested):** يحتفظ بها.
- **الحصص غير المكتَسَبة (Unvested):** تعود للشركة وتُعاد توزيعها أو تُحجَز للموظّفين الجدد.
- **حقّ الشركة بإعادة الشراء:** للشركة الحقّ في إعادة شراء الحصص المكتَسَبة بسعر السوق العادل خلال ٩٠ يوماً من المغادرة.

---

## ٩. اتّخاذ القرارات الكبرى (Major Decisions)

القرارات التالية تتطلّب موافقة ${exitText}:

- بيع الشركة كلّيّاً أو جزئيّاً.
- جمع تمويل خارجي يتجاوز ٢٠٪ من قيمة الشركة.
- إصدار حصص جديدة.
- تغيير النشاط الأساسي للشركة.
- توظيف/فصل أيّ مؤسّس آخر.
- اتّخاذ قروض تتجاوز ١٠٪ من رأس المال.

---

## ١٠. الخروج (Exit Strategy)

- **حقّ الشفعة (Right of First Refusal):** قبل بيع أيّ مؤسّس لحصصه، يجب عرضها أوّلاً على المؤسّسين الآخرين بنفس السعر والشروط.
- **Tag-Along Right:** لو باع أحد المؤسّسين حصّته لطرف خارجي، يحقّ لباقي المؤسّسين بيع حصص متناسبة بنفس السعر.
${input.exitMechanism === "drag-along" ? "- **Drag-Along Right:** عند موافقة أغلبيّة الثلثين على البيع، يلتزم الباقون بالبيع بنفس الشروط." : ""}

---

## ١١. السرّيّة

يلتزم جميع المؤسّسين بالحفاظ على سرّيّة جميع المعلومات التجاريّة والتقنيّة والماليّة الخاصّة بالشركة، خلال فترة العمل وبعدها.

---

## ١٢. التعديل

لا يجوز تعديل هذه الاتّفاقيّة إلّا بموافقة ${exitText} وكتابةً.

---

## التوقيعات

${input.founders.map((f) => `**${f.name}**\nالتاريخ: ____ / ____ / ${new Date().getFullYear()}\nالتوقيع: __________________________\n`).join("\n")}

---

> **تنويه قانوني:** هذه الاتّفاقيّة وثيقة استرشاديّة شاملة، لكنّها **ليست بديلاً عن استشارة محامٍ مختصّ**. ينصح بشدّة بمراجعتها مع محامٍ مرخّص قبل التوقيع، خصوصاً البنود المتعلّقة بالملكيّة الفكريّة والضرائب وآليّة فضّ النزاعات.
> 
> تمّ توليدها بواسطة كلميرون · ${today}`;
}
